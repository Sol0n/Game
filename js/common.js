$(function() {
  $('.1pair').click(function() {
    var wrap = $('.content_1');
    if($(wrap).css('-webkit-transform') == 'matrix(1, 0, 0, 1, 0, 0)') {
      $(wrap).css({'-webkit-transform':'rotateY(180deg)'});
    } else {
      $(wrap).css({'-webkit-transform':'rotateY(0deg)'});
    }
  });
});

